DECLARE
    v_count NUMBER;
    v_duplicates NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
      FROM USER_INDEXES
     WHERE INDEX_NAME = 'UX_BATCH_JOB_RUNNING';
    IF v_count = 0 THEN
        SELECT COUNT(*) INTO v_duplicates
          FROM (
              SELECT DATASET_CD
                FROM BATCH_JOB_LOG
               WHERE STATUS = 'RUNNING'
               GROUP BY DATASET_CD
              HAVING COUNT(*) > 1
          );
        IF v_duplicates > 0 THEN
            RAISE_APPLICATION_ERROR(-20001,
                'UX_BATCH_JOB_RUNNING 생성 전 중복 RUNNING 배치를 정리해야 합니다.');
        END IF;
        EXECUTE IMMEDIATE q'~
            CREATE UNIQUE INDEX UX_BATCH_JOB_RUNNING
                ON BATCH_JOB_LOG (CASE WHEN STATUS = 'RUNNING' THEN DATASET_CD END)
        ~';
    END IF;

    SELECT COUNT(*) INTO v_count
