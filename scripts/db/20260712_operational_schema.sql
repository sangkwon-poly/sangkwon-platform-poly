-- 기존 운영 DB에 최근 애플리케이션 변경을 반영한다.
-- 각 DDL은 USER_* 메타데이터를 확인하므로 적용 도중 중단돼도 같은 파일을 다시 실행할 수 있다.

DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'MEMBER'
       AND COLUMN_NAME = 'VERSION';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE MEMBER ADD (VERSION NUMBER(19,0) DEFAULT 0 NOT NULL)';
    END IF;

    SELECT COUNT(*) INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'ADMIN_USER'
       AND COLUMN_NAME = 'LOCKED_AT';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE ADMIN_USER ADD (LOCKED_AT TIMESTAMP(6))';
    END IF;

    SELECT COUNT(*) INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'INQUIRY'
       AND COLUMN_NAME = 'ANSWER_READ_AT';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE INQUIRY ADD (ANSWER_READ_AT TIMESTAMP(6))';
    END IF;

    SELECT COUNT(*) INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'PAYMENT_ORDER'
       AND COLUMN_NAME = 'SUBSCRIPTION_STARTED_AT';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE PAYMENT_ORDER ADD (SUBSCRIPTION_STARTED_AT TIMESTAMP(6))';
    END IF;

    SELECT COUNT(*) INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'PAYMENT_ORDER'
       AND COLUMN_NAME = 'SUBSCRIPTION_ENDED_AT';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE PAYMENT_ORDER ADD (SUBSCRIPTION_ENDED_AT TIMESTAMP(6))';
    END IF;

    SELECT COUNT(*) INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = 'PAYMENT_ORDER'
       AND COLUMN_NAME = 'VERSION';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE PAYMENT_ORDER ADD (VERSION NUMBER(19,0) DEFAULT 0 NOT NULL)';
    END IF;
END;
