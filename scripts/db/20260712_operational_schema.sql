-- 구독 부여기간 컬럼 도입 전에 승인된 주문도 환불 시 회수할 수 있게 전환한다.
-- 기존 승인 시각을 시작으로 사용하고 결제 주기에 맞춰 종료 시각을 계산한다.
UPDATE PAYMENT_ORDER
   SET SUBSCRIPTION_STARTED_AT = COALESCE(APPROVED_AT, CREATED_AT),
       SUBSCRIPTION_ENDED_AT = ADD_MONTHS(
           COALESCE(APPROVED_AT, CREATED_AT),
           CASE BILLING_CYCLE WHEN 'YEARLY' THEN 12 ELSE 1 END)
 WHERE STATUS = 'PAID'
   AND (SUBSCRIPTION_STARTED_AT IS NULL OR SUBSCRIPTION_ENDED_AT IS NULL);

DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
      FROM PAYMENT_ORDER
     WHERE STATUS = 'PAID'
       AND (SUBSCRIPTION_STARTED_AT IS NULL OR SUBSCRIPTION_ENDED_AT IS NULL);
    IF v_count > 0 THEN
        RAISE_APPLICATION_ERROR(-20002,
            '기존 PAID 주문의 구독 부여기간 전환에 실패했습니다. 결제 데이터를 확인하세요.');
    END IF;
END;
