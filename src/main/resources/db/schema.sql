-- Spring Session JDBC 세션 저장소(재배포/재시작에도 로그인 유지, 다중 인스턴스 대응).
-- Spring Session 공식 Oracle 스키마와 동일. 만료 세션은 Spring Session이 주기적으로 정리한다.
CREATE TABLE SPRING_SESSION (
    PRIMARY_ID            CHAR(36 CHAR)     NOT NULL,
    SESSION_ID            CHAR(36 CHAR)     NOT NULL,
    CREATION_TIME         NUMBER(19,0)      NOT NULL,
    LAST_ACCESS_TIME      NUMBER(19,0)      NOT NULL,
    MAX_INACTIVE_INTERVAL NUMBER(10,0)      NOT NULL,
    EXPIRY_TIME           NUMBER(19,0)      NOT NULL,
    PRINCIPAL_NAME        VARCHAR2(100 CHAR),
    CONSTRAINT SPRING_SESSION_PK PRIMARY KEY (PRIMARY_ID)
);
CREATE UNIQUE INDEX SPRING_SESSION_IX1 ON SPRING_SESSION (SESSION_ID);
CREATE INDEX SPRING_SESSION_IX2 ON SPRING_SESSION (EXPIRY_TIME);
CREATE INDEX SPRING_SESSION_IX3 ON SPRING_SESSION (PRINCIPAL_NAME);

CREATE TABLE SPRING_SESSION_ATTRIBUTES (
    SESSION_PRIMARY_ID CHAR(36 CHAR)      NOT NULL,
    ATTRIBUTE_NAME     VARCHAR2(200 CHAR) NOT NULL,
    ATTRIBUTE_BYTES    BLOB               NOT NULL,
    CONSTRAINT SPRING_SESSION_ATTRIBUTES_PK PRIMARY KEY (SESSION_PRIMARY_ID, ATTRIBUTE_NAME),
    CONSTRAINT SPRING_SESSION_ATTRIBUTES_FK FOREIGN KEY (SESSION_PRIMARY_ID)
        REFERENCES SPRING_SESSION (PRIMARY_ID) ON DELETE CASCADE
);
