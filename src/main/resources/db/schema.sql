  UPDATED_AT        TIMESTAMP(6)      DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT PK_STORE_STAT PRIMARY KEY (STDR_YYQU_CD, TRDAR_CD, INDUTY_CD),
  CONSTRAINT FK_STORE_QU  FOREIGN KEY (STDR_YYQU_CD) REFERENCES DIM_QUARTER (STDR_YYQU_CD),
  CONSTRAINT FK_STORE_TRD FOREIGN KEY (TRDAR_CD)     REFERENCES TRDAR (TRDAR_CD),
  CONSTRAINT FK_STORE_IND FOREIGN KEY (INDUTY_CD)    REFERENCES INDUTY (INDUTY_CD),
  CONSTRAINT CK_STORE_CO_NN   CHECK (STOR_CO>=0 AND SIMILR_INDUTY_STOR_CO>=0 AND OPBIZ_STOR_CO>=0
