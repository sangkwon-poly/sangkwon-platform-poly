  CLSBIZ_RT         NUMBER(7,3),
  OPBIZ_STOR_CO     NUMBER(10,0)      DEFAULT 0 NOT NULL,
  CLSBIZ_STOR_CO    NUMBER(10,0)      DEFAULT 0 NOT NULL,
  FRC_STOR_CO       NUMBER(10,0)      DEFAULT 0 NOT NULL,
