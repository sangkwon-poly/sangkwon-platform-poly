-- Oracle 19c 기준
-- 생성 순서: 기준 데이터 -> 분석 데이터 -> 회원/관리자

-- 기준 데이터
-- 분기 기준
CREATE TABLE DIM_QUARTER (
                             STDR_YYQU_CD   CHAR(5 CHAR)      NOT NULL,
  BASE_YEAR      NUMBER(4,0)       NOT NULL,
  QUARTER_NO     NUMBER(1,0)       NOT NULL,
  QUARTER_LABEL  VARCHAR2(20 CHAR) NOT NULL,
  QU_STRT_DT     DATE,
  QU_END_DT      DATE,
  CREATED_AT     TIMESTAMP(6)      DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT     TIMESTAMP(6)      DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT PK_DIM_QUARTER PRIMARY KEY (STDR_YYQU_CD),
  CONSTRAINT CK_DQ_FORMAT   CHECK (REGEXP_LIKE(STDR_YYQU_CD, '^[0-9]{4}[1-4]$')),
  CONSTRAINT CK_DQ_YEAR     CHECK (BASE_YEAR BETWEEN 2000 AND 2100),
  CONSTRAINT CK_DQ_QNO      CHECK (QUARTER_NO BETWEEN 1 AND 4),
  CONSTRAINT CK_DQ_DERIVED  CHECK (
        BASE_YEAR  = TO_NUMBER(SUBSTR(STDR_YYQU_CD,1,4))
    AND QUARTER_NO = TO_NUMBER(SUBSTR(STDR_YYQU_CD,5,1))),
  CONSTRAINT CK_DQ_DATES    CHECK (QU_STRT_DT IS NULL OR QU_END_DT IS NULL
                                   OR QU_END_DT >= QU_STRT_DT)
);
COMMENT ON TABLE  DIM_QUARTER               IS '분기 차원 테이블. STDR_YYQU_CD = 연도4자리+분기1자리 (예: 20241)';
COMMENT ON COLUMN DIM_QUARTER.STDR_YYQU_CD  IS '기준 년분기 코드 (예: 20241 = 2024년 1분기)';
COMMENT ON COLUMN DIM_QUARTER.BASE_YEAR     IS '연도 (기준코드 앞 4자리)';
COMMENT ON COLUMN DIM_QUARTER.QUARTER_NO    IS '분기 1~4 (기준코드 끝 1자리)';

-- 상권 기본 정보
CREATE TABLE TRDAR (
                       TRDAR_CD        VARCHAR2(20 CHAR)  NOT NULL,
                       TRDAR_CD_NM     VARCHAR2(200 CHAR) NOT NULL,
                       TRDAR_SE_CD     CHAR(1 CHAR)       NOT NULL,
  TRDAR_SE_CD_NM  VARCHAR2(50 CHAR)  NOT NULL,
  SIGNGU_CD       VARCHAR2(20 CHAR),
  SIGNGU_NM       VARCHAR2(50 CHAR),
  ADSTRD_CD       VARCHAR2(20 CHAR),
  ADSTRD_NM       VARCHAR2(50 CHAR),
  CENTER_LOT      NUMBER(11,8),
  CENTER_LAT      NUMBER(10,8),
  GEO_JSON        CLOB,
  CREATED_AT      TIMESTAMP(6)       DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT      TIMESTAMP(6)       DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT PK_TRDAR     PRIMARY KEY (TRDAR_CD),
  CONSTRAINT CK_TRDAR_SE  CHECK (TRDAR_SE_CD IN ('A','D','R','U')),
  CONSTRAINT CK_TRDAR_LOT CHECK (CENTER_LOT IS NULL OR CENTER_LOT BETWEEN -180 AND 180),
  CONSTRAINT CK_TRDAR_LAT CHECK (CENTER_LAT IS NULL OR CENTER_LAT BETWEEN  -90 AND  90),
  CONSTRAINT CK_TRDAR_GEO CHECK (GEO_JSON IS JSON)
);
COMMENT ON TABLE  TRDAR                IS '상권 마스터. GEO_JSON은 상권 경계 폴리곤 좌표(GeoJSON)';
COMMENT ON COLUMN TRDAR.TRDAR_CD       IS '상권 코드 (PK)';
COMMENT ON COLUMN TRDAR.TRDAR_SE_CD    IS '상권 구분: A 골목상권 / D 발달상권 / R 전통시장 / U 관광특구';
COMMENT ON COLUMN TRDAR.SIGNGU_CD      IS '자치구 코드 (COMMERCIAL_RENT/FRANCHISE_COUNT 지역 조인용).';
COMMENT ON COLUMN TRDAR.ADSTRD_CD      IS '행정동 코드 (지역 조인용).';
COMMENT ON COLUMN TRDAR.CENTER_LOT     IS '대표 경도 (지도 핀 배치용; GeoJSON 파싱 불요).';
COMMENT ON COLUMN TRDAR.CENTER_LAT     IS '대표 위도 (지도 핀 배치용).';
COMMENT ON COLUMN TRDAR.GEO_JSON       IS '상권 폴리곤 좌표 (GeoJSON).';
CREATE INDEX IX_TRDAR_SE     ON TRDAR (TRDAR_SE_CD, TRDAR_CD);
CREATE INDEX IX_TRDAR_SIGNGU ON TRDAR (SIGNGU_CD);

